      海南清补凉的食材丰富多样，一般至少有10多种，多的可达20多种。常见的有绿豆、红豆、薏米、通心粉、凉粉块、西米露、汤圆、冬瓜薏、红枣、西瓜丁、葡萄干、鹌鹑蛋、玉米粒、花生仁、百合、银耳、桂圆肉、莲子、菠萝丁、椰子肉等。<br>
