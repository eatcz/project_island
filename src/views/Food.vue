      组合搭配：将煮好、切好的各种食材各抓一点放入碗中，加入几块冰块，再掺入椰子汁、椰奶或红糖水，还可放两勺嫩椰肉，无需搅拌。<br>
      <strong>口味分类</strong><br>
      椰子水清补凉：以新鲜椰子水为基底，味道清甜爽口，充满浓郁的椰子清香，能最大程度地体现清补凉的清爽口感，为食客带来原汁原味的海南风情。<br>
