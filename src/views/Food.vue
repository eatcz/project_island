      配料准备：包括牛肉干、沙虫干、鱼饼、鱿鱼丝、小咸鱼、虾米、芝麻、熟花生仁、酸豆角、酸笋、空心菜、韭菜、香菜等。牛肉和沙虫要经过晒干、油炸，使其色泽金黄、酥脆喷香。<br>
